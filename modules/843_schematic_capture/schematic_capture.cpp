std::mutex g_init_mtx;
bool       g_ready = false;

constexpr const char * KIVER = "20250114";

std::string sesc(std::string_view s) {
    std::string out;
    out.reserve(s.size() + 2);
    for (char c : s) {
        switch (c) {
            case '"':  out += "\\\""; break;
            case '\\': out += "\\\\"; break;
            case '\n': out += "\\n";  break;
            default:   out += c;      break;
        }
    }
    return out;
}

std::string new_uuid() {
    static thread_local std::mt19937_64 rng(std::random_device{}());
    std::uniform_int_distribution<std::uint64_t> dist;
    std::uint64_t a = dist(rng), b = dist(rng);
    a = (a & 0xFFFFFFFFFFFF0FFFULL) | 0x0000000000004000ULL;
    b = (b & 0x3FFFFFFFFFFFFFFFULL) | 0x8000000000000000ULL;
    char buf[37];
    std::snprintf(buf, sizeof(buf),
                  "%08lx-%04lx-%04lx-%04lx-%012lx",
                  static_cast<unsigned long>((a >> 32) & 0xFFFFFFFFULL),
                  static_cast<unsigned long>((a >> 16) & 0xFFFFULL),
                  static_cast<unsigned long>(a & 0xFFFFULL),
                  static_cast<unsigned long>((b >> 48) & 0xFFFFULL),
                  static_cast<unsigned long>(b & 0xFFFFFFFFFFFFULL));
    return std::string(buf, 36);
}

std::string placeholder_lib_symbol(std::string_view lib_id) {
    std::ostringstream os;
    os << "\t\t(symbol \"" << sesc(lib_id) << "\"\n"
       << "\t\t\t(exclude_from_sim no)\n"
       << "\t\t\t(in_bom yes)\n"
       << "\t\t\t(on_board yes)\n"
       << "\t\t\t(property \"Reference\" \"?\" (at 0 0 0) (effects (font (size 1.27 1.27))))\n"
       << "\t\t\t(property \"Value\" \"" << sesc(lib_id) << "\" (at 0 -2.54 0) (effects (font (size 1.27 1.27))))\n"
       << "\t\t\t(property \"Footprint\" \"\" (at 0 0 0) (effects (font (size 1.27 1.27)) (hide yes)))\n"
       << "\t\t\t(property \"Datasheet\" \"\" (at 0 0 0) (effects (font (size 1.27 1.27)) (hide yes)))\n"
       << "\t\t\t(symbol \"" << sesc(lib_id) << "_0_1\"\n"
       << "\t\t\t\t(rectangle (start -2.54 -1.27) (end 2.54 1.27)\n"
       << "\t\t\t\t\t(stroke (width 0.254) (type default))\n"
       << "\t\t\t\t\t(fill (type background))\n"
       << "\t\t\t\t)\n"
       << "\t\t\t)\n"
       << "\t\t\t(embedded_fonts no)\n"
       << "\t\t)\n";
    return os.str();
}

std::string indent_block(std::string_view s, int tabs) {
    std::string prefix(static_cast<std::size_t>(tabs), '\t');
    std::string out;
    out.reserve(s.size() + s.size() / 32);
    bool at_line_start = true;
    for (char c : s) {
        if (at_line_start) { out += prefix; at_line_start = false; }
        out += c;
        if (c == '\n') at_line_start = true;
    }
    if (!out.empty() && out.back() != '\n') out += '\n';
    return out;
}

