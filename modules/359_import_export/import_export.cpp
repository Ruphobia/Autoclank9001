std::string mm(double v) {
    std::ostringstream os;
    os.precision(4);
    os << std::fixed << v;
    // Strip trailing zeros.
    std::string s = os.str();
    while (!s.empty() && s.back() == '0') s.pop_back();
    if (!s.empty() && s.back() == '.') s.pop_back();
    return s;
}

// World-space pad center in mm.
std::pair<double,double> pad_world_mm(const Footprint & fp, const Pad & p) {
    double a = fp.angle.rad();
    double c = std::cos(a), s = std::sin(a);
    double lx = geom::nm_to_mm(p.at.x), ly = geom::nm_to_mm(p.at.y);
    double wx = geom::nm_to_mm(fp.at.x) + lx * c - ly * s;
    double wy = geom::nm_to_mm(fp.at.y) + lx * s + ly * c;
    return { wx, wy };
}

